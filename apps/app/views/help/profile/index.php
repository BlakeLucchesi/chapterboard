<h2 class="title">Profile Help</h2>

<div class="block help">
  <h3>How do I change my password?</h3>
  <p>To change your password, click on “Edit Profile” and then enter your new password in the Password field. Don’t forget to click “Save Profile” at the bottom of the page to save your changes.</p>
</div>

<div class="block help">
  <h3>How do I change my email address?</h3>
  <p>To change your email address, click on “Edit Profile” and then enter your desired email address. Click “Save Profile” at the bottom of the page to save your changes. Please remember that once you change your email address, you will need to use this new email address to login to ChapterBoard.</p>
</div>

<div class="block help">
  <h3>Why isn’t my profile picture uploading?</h3>
  <p>Profile photos must be smaller than 20MB and be one of the following file types: jpg, gif, or png.</p>
</div>

<div class="block help">
  <h3>Why do I need to submit my phone number and carrier?</h3>
  <p>ChapterBoard provides group text messaging to our chapters to help improve member communication. In order to receive these text messages, ChapterBoard users must provide their updated phone number and carrier information.</p>
</div>

<div class="block help">
  <h3>How do I change my name?</h3>
  <p>To discourage members from misrepresenting other individuals, ChapterBoard doesn’t allow our users to change the names on their profiles once they’ve been created. If, however, you’ve misspelled or incorrectly entered your name, please email us at <a href="mailto:team@chapterboard.com">Team@ChapterBoard.com</a> and we can correct it for you.</p>
</div>